import React, { useState } from "react";

const Feedback = () => {
  const [isModalOpen, setIsModalOpen] = useState(false); // State for modal visibility
  const [formContent, setFormContent] = useState(""); // State for the form content
  const [feedbackData, setFeedbackData] = useState([]); // Initial feedback data

  const handleOpenModal = () => {
    setFormContent(""); // Clear the form when opening the modal
    setIsModalOpen(true);
  };

  const handleCloseModal = () => {
    setIsModalOpen(false);
  };

  const handleSubmit = (event) => {
    event.preventDefault();

    // Add the new feedback to the list
    setFeedbackData((prevFeedback) => [
      ...prevFeedback,
      {
        name: "Gabriel Q. Bruzula", // Example name
        username: "@gbruzula", // Example username
        content: formContent, // User's feedback
      },
    ]);

    setFormContent(""); // Clear the textarea
    setIsModalOpen(false); // Close the modal
  };

  return (
    <div className="min-h-screen flex flex-col items-center px-4 pt-8 overflow-hidden">
      {/* Header Section */}
      <h1 className="text-2xl font-semibold text-gray-800 mb-2 text-center">
        Our Feedback
      </h1>
      <p className="text-gray-600 text-center max-w-2xl">
        We value your input on mapHappens. Your feedback will help us improve. Please share your thoughts and suggestions
        to make mapHappens even better!
      </p>

      {/* Feedback List */}
      <div className="mt-6 w-full max-w-4xl space-y-4">
        {feedbackData.map((feedback, index) => (
          <div
            key={index}
            className="bg-white shadow-md p-4 rounded-md flex items-start space-x-4"
          >
            <div className="flex items-center justify-center w-12 h-12 rounded-full bg-blue-100 text-blue-600 text-lg font-semibold">
              {feedback.name.split(" ")[0][0]}
              {feedback.name.split(" ")[1][0]}
            </div>
            <div>
              <h4 className="font-semibold text-gray-800">{feedback.name}</h4>
              <span className="text-sm text-gray-500">{feedback.username}</span>
              <p className="mt-2 text-gray-700">{feedback.content}</p>
            </div>
          </div>
        ))}
      </div>

      {/* Floating Plus Button */}
      <button
        onClick={handleOpenModal}
        className="fixed bottom-6 right-6 w-16 h-16 rounded-full bg-blue-600 text-white shadow-lg flex items-center justify-center hover:bg-blue-700 focus:outline-none focus:ring-2 focus:ring-blue-500 focus:ring-offset-2"
        aria-label="Add Feedback"
      >
        <svg
          xmlns="http://www.w3.org/2000/svg"
          fill="none"
          viewBox="0 0 24 24"
          strokeWidth={2}
          stroke="currentColor"
          className="w-8 h-8"
        >
          <path strokeLinecap="round" strokeLinejoin="round" d="M12 4v16m8-8H4" />
        </svg>
      </button>

      {/* Modal Form */}
      <div
        className={`fixed inset-0 bg-black bg-opacity-50 flex items-center justify-center z-50 transition-opacity duration-300 ${
          isModalOpen ? "opacity-100" : "opacity-0 pointer-events-none"
        }`}
      >
        <div
          className={`bg-white w-full sm:max-w-md md:max-w-lg lg:max-w-3xl p-8 rounded-lg shadow-xl transform transition-all duration-500 ${
            isModalOpen ? "scale-100" : "scale-90"
          }`}
        >
          <h3 className="text-2xl font-semibold text-gray-800">Gabriel Q. Bruzula</h3>
          <p className="text-lg text-gray-500 mb-4">@gbruzula</p>
          <form onSubmit={handleSubmit}>
            <label htmlFor="feedback" className="block text-lg text-gray-700 mb-3">
              Tell us your experience using mapHappens.
            </label>
            <textarea
              id="feedback"
              rows="6"
              value={formContent} // Bind the textarea value to formContent
              onChange={(e) => setFormContent(e.target.value)} // Update the formContent state
              placeholder="Type here..."
              className="w-full p-4 border rounded-md text-lg focus:outline-none focus:ring-2 focus:ring-blue-500"
              required
            ></textarea>
            <div className="flex justify-end space-x-6 mt-6">
              <button
                type="button"
                onClick={handleCloseModal}
                className="text-lg text-gray-600 hover:underline"
              >
                Cancel
              </button>
              <button
                type="submit"
                className="bg-green-600 text-white px-6 py-3 rounded-md text-lg hover:bg-green-700"
              >
                Submit
              </button>
            </div>
          </form>
        </div>
      </div>
    </div>
  );
};

export default Feedback;

